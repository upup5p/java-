package basic01;

public class Demo06 {
    //while循环
    public static void main(String[] args) {
        int i = 1;
        while (i <= 100) {
            System.out.println(i);
            i++;
        }
        //do while循环
        int a = 0;
        do {
            System.out.println("output" + a);
            a++;
        } while (a <= 5);

    }
}
